import { readFile, writeFile } from "node:fs/promises";

const profileUrl = "https://scholar.google.com/citations?hl=en&user=a-fHvRUAAAAJ";
const outputPath = new URL("../assets/scholar-stats.js", import.meta.url);
const softFail =
  process.env.SCHOLAR_UPDATE_SOFT_FAIL === "1" ||
  process.env.SCHOLAR_UPDATE_SOFT_FAIL === "true" ||
  process.env.GITHUB_ACTIONS === "true";

function parseMetric(html, label) {
  const escapedLabel = label.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
  const pattern = new RegExp(
    `>${escapedLabel}<\\/a><\\/td><td class="gsc_rsb_std">(\\d+)`,
    "i"
  );
  const match = html.match(pattern);
  if (!match) throw new Error(`Could not find ${label} on the Scholar profile.`);
  return Number(match[1]);
}

function parseStats(html) {
  const years = [...html.matchAll(/class="gsc_g_t"[^>]*>(20\d{2})<\/span>/g)].map((match) => match[1]);
  const yearlyValues = [...html.matchAll(/class="gsc_g_al">(\d+)<\/span>/g)].map((match) => Number(match[1]));
  const outputMatch = html.match(/id="gsc_a_nn">Articles 1(?:&ndash;|-)\s*(\d+)/);

  if (years.length === 0 || years.length !== yearlyValues.length) {
    throw new Error("Scholar citation history was missing or malformed.");
  }
  if (!outputMatch) throw new Error("Could not find the Scholar output count.");

  return {
    citations: parseMetric(html, "Citations"),
    hIndex: parseMetric(html, "h-index"),
    i10Index: parseMetric(html, "i10-index"),
    outputs: Number(outputMatch[1]),
    citationsByYear: Object.fromEntries(years.map((year, index) => [year, yearlyValues[index]]))
  };
}

function parseCurrentStats(source) {
  const match = source.match(/window\.SCHOLAR_STATS\s*=\s*({[\s\S]*});/);
  if (!match) return null;
  return JSON.parse(match[1]);
}

async function fetchProfile() {
  let lastError;

  for (let attempt = 1; attempt <= 3; attempt += 1) {
    try {
      const response = await fetch(profileUrl, {
        headers: {
          "User-Agent": "Mozilla/5.0 (compatible; AntonySite-ScholarTracker/1.0)",
          "Accept-Language": "en-US,en;q=0.9"
        },
        signal: AbortSignal.timeout(20_000)
      });

      if (!response.ok) throw new Error(`Scholar returned HTTP ${response.status}.`);
      return await response.text();
    } catch (error) {
      lastError = error;
      if (attempt < 3) await new Promise((resolve) => setTimeout(resolve, attempt * 2_000));
    }
  }

  throw lastError;
}

const currentSource = await readFile(outputPath, "utf8").catch(() => "");
const currentStats = parseCurrentStats(currentSource);

let nextStats;

try {
  const html = await fetchProfile();
  nextStats = parseStats(html);
} catch (error) {
  if (softFail && currentStats) {
    console.warn(`Scholar metrics could not be refreshed: ${error.message}`);
    console.warn(`Keeping existing Scholar metrics from ${currentStats.updated || "the current snapshot"}.`);
    process.exit(0);
  }

  throw error;
}

const comparableCurrent = currentStats && {
  citations: currentStats.citations,
  hIndex: currentStats.hIndex,
  i10Index: currentStats.i10Index,
  outputs: currentStats.outputs,
  citationsByYear: currentStats.citationsByYear
};

if (JSON.stringify(comparableCurrent) === JSON.stringify(nextStats)) {
  console.log("Scholar metrics are unchanged.");
  process.exit(0);
}

const generatedStats = {
  ...nextStats,
  updated: new Date().toISOString().slice(0, 10)
};
const source = `// Generated by scripts/update-scholar-stats.mjs. Do not edit manually.\nwindow.SCHOLAR_STATS = ${JSON.stringify(generatedStats, null, 2)};\n`;

await writeFile(outputPath, source, "utf8");
console.log(`Updated Scholar metrics: ${generatedStats.citations} citations.`);
